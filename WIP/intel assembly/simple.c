#include <stdio.h>

int main()
{
    int a = 100;
    int b = 200;
    int c = 0;

    c = a + b;  // 100 + 200 = 300
    c *= b;     // 300 * 200 = 60000
    c /= 2;     // 60000 / 100 = 600
    c += 20;    // 600 + 20 = 620
    a += 23;    // 100 + 23 = 123
    c += a * b; // 620 + (123 * 200)

    printf("%d\n", c);
}