int main()
{
    int a = 100;
    int b = 200;
    int c = 0;

    c = a + b;  // 100 + 200 = 300
    c *= b;     // 300 * 200 = 60000
    c /= 2;     // 60000 / 2 = 30000
    c += 20;    // 30000 + 20 = 30020
    a += 23;    // 100 + 23 = 123
    c += a * b; // 30020 + (123 * 200)

    return c;
}
